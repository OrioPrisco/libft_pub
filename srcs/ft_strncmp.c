#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	len;

	if (!n)
		return (0);
	len = ft_strnlen(s1, ft_strnlen(s2, n));
	if (len < n)
		len++;
	return (ft_memcmp(s1, s2, len));
}
